// Cloudflare AI 的 toMarkdown 轉 PDF 時開頭會塞一段檔案 metadata（# 檔名 →
// ## Metadata → 一堆 "- Key=Value"）。搜尋時這些是雜訊，還會讓「檔名裡的關鍵字」
// 永遠命中在最上面、snippet 永遠停在 metadata。讀取／比對前剝掉，只留本文。
// 只剝 "- key=value" 型條列與檔名 H1，不誤傷本文項目符號（「- 产品介绍」保留）。
// 用於「即時清洗現有已存的髒資料」，不必等使用者重跑整理。找不到就原樣回傳。
export function stripPdfMetadata(md) {
  const lines = String(md || "").split("\n");
  let i = 0;
  while (i < lines.length && (lines[i].trim() === "" || lines[i].startsWith("# "))) i++;
  if (i < lines.length && /^##\s*Metadata/i.test(lines[i].trim())) {
    i++;
    while (i < lines.length) {
      const t = lines[i].trim();
      if (t === "" || /^-\s+[\w.:@-]+=/.test(t)) { i++; continue; }
      break;
    }
  }
  const body = lines.slice(i).join("\n").trim();
  // 圖形型 PDF（無文字層）：toMarkdown 只吐得出頁面骨架（## Contents / ### Page N），
  // 沒有真正的內文段落。這種「只有標題、沒有內文」的結果對搜尋無意義，視為無內容。
  const hasProse = body.split("\n").some((l) => l.trim() && !l.trimStart().startsWith("#"));
  return hasProse ? body : "";
}

// 全形 ASCII（！～ 區段，含全形數字字母標點）→ 半形，全形空格 → 半形空格
function toHalfWidth(ch) {
  const code = ch.charCodeAt(0);
  if (code >= 0xff01 && code <= 0xff5e) return String.fromCharCode(code - 0xfee0);
  if (code === 0x3000) return " ";
  return ch;
}

/**
 * 摺疊：繁→簡、全形→半形、大寫→小寫。嚴格保持「1 UTF-16 單位 → 1 單位」，
 * 索引與長度不變，方便用摺疊後的比對結果回切原文。
 */
export function foldText(s) {
  let out = "";
  for (const ch of String(s ?? "")) {
    out += T2S[ch] || toHalfWidth(ch);
  }
  return out.toLowerCase();
}

// 摺疊後的關鍵字前後文擷取：在摺疊文裡定位，回切「原文」（長度對齊，索引通用）
export function foldSnippet(rawText, foldedQuery, ctx = 80) {
  const raw = String(rawText ?? "");
  const folded = foldText(raw);
  const i = folded.indexOf(foldedQuery);
  const collapse = (x) => x.replace(/\s+/g, " ");
  if (i < 0) {
    const t = collapse(raw);
    return t.length > ctx * 2 ? t.slice(0, ctx * 2) + "…" : t;
  }
  const start = Math.max(0, i - ctx);
  const end = Math.min(raw.length, i + foldedQuery.length + ctx);
  return (start > 0 ? "…" : "") + collapse(raw.slice(start, end)) + (end < raw.length ? "…" : "");
}
